nomeDoAluno = str(input('Digite o nome do aluno: '))
primeiraNota = float(input('Digite a primeira nota do aluno: '))
segundaNota = float(input('Digite a segunda nota do aluno: '))
terceiraNota = float(input('Digite a segunda nota do aluno: '))
media = (primeiraNota + segundaNota + terceiraNota) / 3
print(f'O aluno {nomeDoAluno} obteve a media de {media:.2f}')


